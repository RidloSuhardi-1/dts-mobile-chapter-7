package com.example.androidlistview;

public class Negara {
    public String negara;
    public String ibuKota;

    public Negara(String negara, String ibuKota) {
        this.negara = negara;
        this.ibuKota = ibuKota;
    }

    public String getNegara() {
        return negara;
    }

    public String getIbuKota() {
        return ibuKota;
    }

}
